#lambda function

# A lambda function in Python is a small, anonymous function that is defined without a name
#  It's often used for short, simple operations that are meant to be used temporarily.

#    lambda arguments: expression


#Create a lambda function to find the cube of the number 

cube = lambda x: x ** 3
print(cube(3))

# Another example : Write a lambda function to add a two number 

add = lambda x , y: x + y
print(add(5 , 6))